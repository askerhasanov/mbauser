class CourseBasicData {

  final String id;
  final String name;
  final String image;
  final String price;
  final String rating;
  final String lessons;
  final String days;
  final String exams;

  CourseBasicData({
    required this.id,
    required this.name,
    required this.image,
    required this.price,
    required this.rating,
    required this.lessons,
    required this.days,
    required this.exams,
  });
}
